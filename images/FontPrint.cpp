void printText(char* text, uint16_t x_pos, uint16_t y_pos, uint16_t bg_color, uint16_t text_color) {
    while (*text != 0) {
        if (*text == ' ') {
            x_pos += 2;
        } else {
               
            uint16_t index;
            if (*text < '9') {
                index = 9 - (*text - '0');
            } else {
                index = 35 - (*text - 'A');
            }
            index *= 7 * 4;
            uint16_t temp[7 * 4];
            for (uint32_t row = 0; row < 7; row++) {
                for (uint32_t column = 0; column < 4; column++) {
                    if (font[index + (row * 4 + column)] == 0xFFFF) {
                        temp[row * 4 + column] = text_color;
                    } else {
                        temp[row * 4 + column] = bg_color;
                    }
                }
            }
            ST7735_DrawBitmap(x_pos, y_pos, temp, 4, 7);
            x_pos += 5;
        }
        text++;
    }
}
